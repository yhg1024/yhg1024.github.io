package com.momo.vo;

/**
 * 
 * DTO/VO : 데이터를 수집 하는 그릇
 * 
 */
public class UserVO {
	String name;
	int age;
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	
}
